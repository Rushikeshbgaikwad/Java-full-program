package com.course;

public class datatypes {
    public static void main(String[] args) {
        //primitive types
        byte age = 23;
        short random = 15968;
        int phone = 8459652;
        long give = 84596523698745L;
        float pi = 3.14F;
        double pii = 3.148949D;
        char ch = '@';
        boolean agex = true ;
       // non-primitive types
        String first = "Rushikesh";
        String last = "Gaikwad";
        String middle = new String("Babu");
        System.out.println(middle.length());
    }
}
